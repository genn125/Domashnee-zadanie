# Практика: "Строки и индексация строк"

example = 'Полимер'

print(example [0]) # первый символ этой строки.

print(example [-1]) # последний символ этой строки

print(example[len(example) // 2:])# вторая половина этой строки, исправлено преподавателем

print(example [::-1]) # это слово наоборот

print(example [::2]) # каждый второй символ этой строки


